// Function to change the language
function changeLanguage(lang) {
    if (!translations[lang]) {
        console.error('Translation not available for:', lang);
        return;
    }
    
    // Loop through all elements with data-i18n attribute
    document.querySelectorAll('[data-i18n]').forEach(element => {
        const key = element.getAttribute('data-i18n');
        if (translations[lang][key]) {
            // Check if the element is an input with placeholder
            if (element.tagName === 'INPUT' && element.hasAttribute('placeholder')) {
                element.placeholder = translations[lang][key];
            } 
            // Check if it's a button with value
            else if (element.tagName === 'BUTTON' && element.hasAttribute('value')) {
                element.value = translations[lang][key];
            }
            // For all other elements, set inner text
            else {
                element.textContent = translations[lang][key];
            }
        }
    });
    
    // Store the language preference in local storage
    localStorage.setItem('edumetrics_language', lang);
    
    // Optional: Update page title and meta description
    if (lang === 'en') {
        document.title = 'EduMetrics - Educational Assessment Platform';
        document.querySelector('meta[name="description"]').setAttribute('content', 
            'EduMetrics is an innovative educational assessment platform that uses artificial intelligence to optimize the assessment process.');
    } else {
        document.title = 'EduMetrics - Αξιολόγηση Εκπαιδευτικού Έργου';
        document.querySelector('meta[name="description"]').setAttribute('content', 
            'Το EduMetrics είναι μια πρωτοποριακή πλατφόρμα αξιολόγησης εκπαιδευτικών που χρησιμοποιεί τεχνητή νοημοσύνη για τη βελτιστοποίηση της διαδικασίας αξιολόγησης.');
    }
    
    // Fire a custom event that other scripts can listen for
    document.dispatchEvent(new CustomEvent('languageChanged', { detail: { language: lang } }));
}
